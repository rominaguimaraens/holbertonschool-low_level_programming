#include <stdio.h>
/**
 * op_add - adds up
 * @a: 1st number
 * @b: 2nd number
 * Return: result
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - subtracts
 * @a: 1st number
 * @b: 2nd number
 * Return: result
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplies
 * @a: 1st number
 * @b: 2nd number
 * Return: result
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divides
 * @a: 1st number
 * @b: 2nd number
 * Return: result
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - gets module
 * @a: 1st number
 * @b: 2nd number
 * Return: result
 */
int op_mod(int a, int b)
{
	return (a % b);
}
